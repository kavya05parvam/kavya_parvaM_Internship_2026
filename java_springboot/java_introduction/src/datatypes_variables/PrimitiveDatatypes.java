package datatypes_variables;

public class PrimitiveDatatypes {
	public static void main(String[] args) {
		// byte

//		 byte age;
//		 age = 30;

		byte age = 30; // same line declaration and initialization
		System.out.println("byte: " + age);
		short var1 = 40;
		System.out.println("short: " + var1);
		int total = 50;
		System.out.println("int: " + total);
		long var2 = 4000;
		System.out.println("long: " + var2);
		float marks = 20.60f;
		System.out.println("float: " + marks);
		double marks1 = 120.50;
		System.out.println("Double: " + marks1);
		char gender = 'M';
		System.out.println("Char: " + gender);
		boolean isActive = false;
		System.out.println("bboolean: " + isActive);
	}
}

package task_operators;

public class assignment_operators {
	public static void main(String[] args) {
        int num = 10;

        num += 5;
        System.out.println("After += : " + num);

        num -= 3;
        System.out.println("After -= : " + num);

        num *= 2;
        System.out.println("After *= : " + num);
    }

}
